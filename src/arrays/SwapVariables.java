package arrays;

public class SwapVariables {

    public static void main(String[] args) {



        // Swap 2 variable values

        int a = 33;
        int b = 44;

        // Swap variable values

        int temp = a;
        a = b;
        b = temp;

        System.out.println("a: " + a); // 44
        System.out.println("b: " + b); // 33

    }
}
