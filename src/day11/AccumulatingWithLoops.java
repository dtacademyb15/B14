package day11;

public class AccumulatingWithLoops {

    public static void main(String[] args) {


        // Find the sum of numbers from 1-1000

        int i = 1;
        int sum = 0;
        while(i <= 1000){
            sum = sum + i; // 1, 3, 6,...
            System.out.println("In repetition " + i + " the sum is " + sum);
            i++;
        }

        System.out.println("The sum is:" + sum);









//





    }
}
